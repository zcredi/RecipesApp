    for char in smile {
        
        switch char {
        case "üôÇ":
            smileSum += 1
        case "üòÇ":
            smileSum += 5
        case "üòò":
            smileSum += 10
        case "üòÅ":
            smileSum += 50
        case "üòé":
            smileSum += 100
        case "ü§™":
